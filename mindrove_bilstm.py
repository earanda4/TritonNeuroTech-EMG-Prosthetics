def load_continuous_60hz_custom_filter(file_path, fs=60.0):
    """Loads RAW channels and applies YOUR custom filter."""
    print(f"Processing RAW 60Hz file: {os.path.basename(file_path)}")
    df = pd.read_csv(file_path, sep='\t')
    raw_cols = ['Channel1', 'Channel2', 'Channel3', 'Channel4']
    emg_raw = df[raw_cols].values

    # Apply custom filter
    emg_filtered = filter_emg_data(emg_raw, fs=fs)
    df_emg = pd.DataFrame(emg_filtered, columns=['CH1', 'CH2', 'CH3', 'CH4'])

    # Labeling intervals (Relax, Fist, Rock, Peace, Shaka)
    intervals = [(0,10,0), (10,30,1), (30,40,0), (40,60,2), (60,70,0), (70,90,3), (90,100,0), (100,120,4)]
    df_emg['Class'] = -1
    for s, e, l in intervals:
        s_idx, e_idx = int(s*fs), int(e*fs)
        if s_idx < len(df_emg):
            df_emg.iloc[s_idx:min(e_idx, len(df_emg)), df_emg.columns.get_loc('Class')] = l

    df_emg = df_emg[df_emg['Class'] != -1].copy()
    scaler = StandardScaler()
    df_emg[['CH1', 'CH2', 'CH3', 'CH4']] = scaler.fit_transform(df_emg[['CH1', 'CH2', 'CH3', 'CH4']])
    return df_emg

