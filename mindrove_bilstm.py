# --- 5. MODEL & TRAINER ---

class FinalPushLSTM(nn.Module):
    def __init__(self, input_size=4, hidden_size=128, num_layers=2, num_classes=4):
        super().__init__()
        self.lstm = nn.LSTM(input_size, hidden_size, num_layers, batch_first=True, dropout=0.4, bidirectional=True)
        self.fc = nn.Sequential(
            nn.Linear(hidden_size * 2, 256), nn.BatchNorm1d(256), nn.ReLU(),
            nn.Dropout(0.5), nn.Linear(256, num_classes)
        )
    def forward(self, x):
        self.lstm.flatten_parameters()
        out, _ = self.lstm(x)
        return self.fc(out[:, -1, :])

def run_experiment(X, y, name):
    # device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    device = torch.device("mps" if torch.backends.mps.is_available() else "cuda" if torch.cuda.is_available() else "cpu")
    X_np, y_np = X.cpu().numpy(), y.cpu().numpy()
    X_train, X_val, y_train, y_val = train_test_split(X_np, y_np, test_size=0.2, random_state=42, stratify=y_np)

    train_loader = DataLoader(TensorDataset(torch.tensor(X_train), torch.tensor(y_train)), batch_size=256, shuffle=True)
    val_loader = DataLoader(TensorDataset(torch.tensor(X_val), torch.tensor(y_val)), batch_size=256)

    model = FinalPushLSTM(num_classes=int(y_np.max() + 1)).to(device)
    optimizer = torch.optim.AdamW(model.parameters(), lr=0.001, weight_decay=0.01)
    criterion = nn.CrossEntropyLoss(label_smoothing=0.1)
    scheduler = torch.optim.lr_scheduler.OneCycleLR(optimizer, max_lr=0.003, steps_per_epoch=len(train_loader), epochs=15)

    history = {'train_loss': [], 'train_acc': [], 'val_loss': [], 'val_acc': [], 'lrs': []}
    print(f"\n{'='*20} Starting Training: {name} {'='*20}")

    for epoch in range(15):
        model.train()
        t_loss, t_corr, t_total = 0, 0, 0
        for bx, by in train_loader:
            bx, by = bx.to(device), by.to(device)
            optimizer.zero_grad(); outputs = model(bx); loss = criterion(outputs, by); loss.backward(); optimizer.step()
            history['lrs'].append(optimizer.param_groups[0]['lr']); scheduler.step()
            t_loss += loss.item(); t_corr += (outputs.argmax(1) == by).sum().item(); t_total += by.size(0)

        model.eval()
        v_loss, v_corr, v_total = 0, 0, 0
        with torch.no_grad():
            for vx, vy in val_loader:
                vx, vy = vx.to(device), vy.to(device)
                v_out = model(vx); v_loss += criterion(v_out, vy).item()
                v_corr += (v_out.argmax(1) == vy).sum().item(); v_total += vy.size(0)

        history['train_loss'].append(t_loss/len(train_loader)); history['train_acc'].append((t_corr/t_total)*100)
        history['val_loss'].append(v_loss/len(val_loader)); history['val_acc'].append((v_corr/v_total)*100)
        print(f"Epoch {epoch+1:02d} | T-Loss: {history['train_loss'][-1]:.3f} | V-Acc: {history['val_acc'][-1]:.2f}%")

    # --- VISUALIZATION ---
    plt.figure(figsize=(18, 5))
    plt.subplot(1, 3, 1); plt.plot(history['train_loss'], label='Train'); plt.plot(history['val_loss'], label='Val'); plt.title('Loss'); plt.legend()
    plt.subplot(1, 3, 2); plt.plot(history['train_acc'], label='Train'); plt.plot(history['val_acc'], label='Val'); plt.title('Accuracy'); plt.legend()
    plt.subplot(1, 3, 3); plt.plot(history['lrs']); plt.title('OneCycle LR')
    plt.tight_layout(); plt.show()

    plt.figure(figsize=(8, 6))
    cm = confusion_matrix(y_val, model(torch.tensor(X_val).to(device)).argmax(1).cpu().numpy())
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues'); plt.title(f'CM: {name}'); plt.show()
    return history['val_acc'][-1]

